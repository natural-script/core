//----------------------------------------------------------Smooth Scrolling------------------------------------------------------------------------------------------------------------------------------------------//
//--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
$('a').click(function() {
	if ($(this).attr('href').indexOf('#') > -1) {
		$('html, body').animate({
			scrollTop: $($(this).attr('href')).offset().top
		}, 500);
	}
	return false;
});