	window.scale = function (elementName, type, value) {
		if (type == 'length') {
			anime({
				targets: '#' + elementName + '',
				height: value
			});
		} else if (type == 'width') {
			anime({
				targets: '#' + elementName + '',
				color: value
			});
		} else if (type == 'zoom') {
			anime({
				targets: '#' + elementName + '',
				scale: value
			});
		}
	};